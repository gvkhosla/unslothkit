def create_project(path: Path, task: str, model_label: str, force: bool = False) -> None:
    if path.exists() and any(path.iterdir()) and not force:
        raise FileExistsError(f"{path} already exists and is not empty; pass --force to overwrite generated files")
    path.mkdir(parents=True, exist_ok=True)
    (path / "data").mkdir(exist_ok=True)
    (path / "adapters").mkdir(exist_ok=True)
    (path / "notes").mkdir(exist_ok=True)

    cfg = default_config(project_name=path.name, task=task, model_label=model_label)
    (path / "config.json").write_text(json.dumps(cfg, indent=2) + "\n", encoding="utf-8")
    (path / "train.py").write_text(TRAIN_PY, encoding="utf-8")
    (path / "chat.py").write_text(CHAT_PY, encoding="utf-8")
    (path / "eval.py").write_text(EVAL_PY, encoding="utf-8")
    (path / "START_HERE.md").write_text(START_HERE.format(task=task), encoding="utf-8")
    (path / "README.md").write_text(PROJECT_README.format(project_name=path.name, task=task, task_hint=task_hint(task)), encoding="utf-8")
    write_sample(path / "data" / "train.jsonl", task=task)
    write_sample(path / "data" / "eval.jsonl", task=task)
    (path / "notes" / "plan.md").write_text(dedent(f"""
    # Fine-tuning plan

    ## Goal
    Build a better model for `{task}`.

    ## Baseline
    - Run `python eval.py --base` before training.
    - Save examples where the base model fails.

    ## First experiment
    - Model: `{cfg['base_model']}`
    - Steps: `{cfg['max_steps']}`
    - LoRA rank: `{cfg['lora_rank']}`
    - Success: eval examples and manual chat feel closer to the desired behavior.

    ## Next iteration
    - Add 20-100 higher-quality examples from failure cases.
    - Keep eval data separate.
    - Compare base vs adapter before increasing model size.
    """).lstrip(), encoding="utf-8")
